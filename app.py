ps = PorterStemmer()

def transform_text(text):
    text = text.lower()
    text = text.split()  # ‚úÖ Replacing nltk.word_tokenize with basic split

    y = []
    for i in text:
        i = ''.join(char for char in i if char.isalnum())  # remove punctuations
        if i and i not in english_stopwords:
            y.append(ps.stem(i))

    return " ".join(y)

# Load models
